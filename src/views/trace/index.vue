const transformApiData = (apiData: TraceDetailResult): TraceData => {
  const { foodInfo, cropBatch, pastureInfo, batchTaskDetailList } = apiData
  let taskList = batchTaskDetailList || []
  // 计算异常次数（已禁用显示）
  const errorCount = 0

  // 根据食品类型对任务进行排序
  // fishDish: 0=鱼, 1=菜
  // 从 foodInfo 中获取食品类型信息，如果没有则从其他字段推断
  const foodType = (foodInfo as any)?.foodType || 
    ((foodInfo as any)?.cuisineStatus !== undefined ? 'cuisine' : 
     (foodInfo as any)?.fishStatus !== undefined ? 'fish' : 'unknown')
  
  if (foodType === 'fish') {
    // 如果是鱼，鱼的任务(fishDish=0)排在前面
    taskList = [...taskList].sort((a, b) => {
      const aFishDish = Number(a.batchTask?.fishDish) ?? 1
      const bFishDish = Number(b.batchTask?.fishDish) ?? 1
      return aFishDish - bFishDish // 0(鱼)排在1(菜)前面
    })
  } else if (foodType === 'cuisine') {
    // 如果是菜，菜的任务(fishDish=1)排在前面
    taskList = [...taskList].sort((a, b) => {
      const aFishDish = Number(a.batchTask?.fishDish) ?? 0
      const bFishDish = Number(b.batchTask?.fishDish) ?? 0
      return bFishDish - aFishDish // 1(菜)排在0(鱼)前面
    })
  }

  // timeline 映射每个批次任务
  const timeline = taskList.map(taskDetail => {
    const { batchTask, weatherAvg, waterQualityAvg, sensorAlertList } = taskDetail
    let type: 'success' | 'error' | 'warning' = 'success'
    if (batchTask?.status === '2') type = 'error'
    else if (batchTask?.status === '1') type = 'warning'
    const images = batchTask?.taskImages ? [{
      url: batchTask.taskImages,
      description: `${batchTask?.taskName || ''}操作图片`
    }] : undefined
    const videos = batchTask?.taskVideos ? [{
      url: batchTask.taskVideos,
      description: `${batchTask?.taskName || ''}操作视频`
    }] : undefined
    // 时间范围拼接
    let timeRange = '';
    if (batchTask?.actualStart && batchTask?.actualFinish) {
      timeRange = `${batchTask.actualStart} ~ ${batchTask.actualFinish}`;
    } else if (batchTask?.planStart && batchTask?.planFinish) {
      timeRange = `${batchTask.planStart} ~ ${batchTask.planFinish}`;
    } else {
      timeRange = batchTask?.actualStart || batchTask?.actualFinish || batchTask?.planStart || batchTask?.planFinish || '';
    }
    return {
      time: timeRange,
      title: batchTask?.taskName || '',
      description: batchTask?.taskDetail || batchTask?.taskName || '',
      operator: batchTask?.responsiblePersonName || '',
      type,
      images,
      videos,
      environment: {
        temperature: weatherAvg?.temperature ?? 0,
        humidity: weatherAvg?.humidity ?? 0,
        ph: waterQualityAvg?.phValue ?? 0,
        dissolvedOxygen: waterQualityAvg?.dissolvedOxygen ?? 0,
        lightIntensity: weatherAvg?.lightIntensity ?? 0,
        windSpeed: weatherAvg?.windSpeed ?? 0,
        waterTemperature: waterQualityAvg?.waterTemperature ?? 0,
        ammoniaNitrogen: waterQualityAvg?.ammoniaNitrogen ?? 0
      },
      sensorAlertList: sensorAlertList || [] 
    }
  })

  return {
