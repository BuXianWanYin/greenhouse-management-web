const envDetailStarsForDialog = computed(() => {
  if (!currentTimelineDetail.value || !traceData.value) return [];
  const list = traceData.value.thresholdConfigList || [];
  const env: Record<string, number> = (currentTimelineDetail.value.environment || {}) as Record<string, number>;
  const alertList = currentTimelineDetail.value.sensorAlertList || [];
  return envParams.map(param => {
    const config = list.find((item: any) => item.paramType === param.paramType);
    const value = env[param.key] ?? null;
    let score = calcScore(value, config, param.paramType);
    const alerts = getAlertsByParam(param.paramType, alertList);
    if (alerts.length > 0) {
      const maxLevel = Math.max(...alerts.map(a => Number(a.alertLevel ?? 0)));
      if (maxLevel === 1) score = 40;
      else score = 60;
    }
    // 智能建议：有异常优先显示异常提示
    let advice = getEnvAdvice(param.paramType, value, config);
    if (alerts.length > 0) {
      const maxLevel = Math.max(...alerts.map(a => Number(a.alertLevel ?? 0)));
      if (maxLevel === 1) {
        advice = '有严重异常，需关注';
      } else {
        advice = '有异常记录，需关注';
      }
      // 或者显示最近一条异常信息：
      // advice = alerts[0].alertMessage || '有异常记录，需关注';
    }
    const stars = scoreToStars(score);
    return {
      ...param,
      value,
      config,
      score,
      stars,
      advice,
      thresholdDesc: getThresholdDesc(param.paramType),
      alertList
    };
  });
});

