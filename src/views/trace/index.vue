// 获取适宜区间说明
function getThresholdDesc(paramType: string) {
  const list = traceData.value?.thresholdConfigList || [];
  const config = list.find((item: any) => item.paramType === paramType);
  if (!config) return '';
  if (paramType === 'ph_value') {
    return `适宜区间：${config.thresholdMin}~${config.thresholdMax}`;
  }
  return `适宜区间：${config.thresholdMin}~${config.thresholdMax}${config.unit || ''}`;
}

// 健康分数、星级、智能说明工具函数
function calcScore(value: number, config: any, paramType?: string) {
  if (config == null) return 0;
  value = Number(value);
  const min = Number(config.thresholdMin);
  const max = Number(config.thresholdMax);
  if (isNaN(value) || isNaN(min) || isNaN(max)) return 0;
  if (value == null) return 0;
  if (value === 0 && paramType !== 'wind_speed') return 0;
  if (value >= min && value <= max) return 100;
  const range = max - min || 1;
  if (value < min) return Math.max(0, 100 - ((min - value) / range) * 100);
  if (value > max) return Math.max(0, 100 - ((value - max) / range) * 100);

  return 0;
}
function scoreToStars(score: number) {
  if (score >= 90) return 5;
  if (score >= 75) return 4;
  if (score >= 60) return 3;
  if (score >= 40) return 2;
  if (score > 0) return 1;
  return 0;
}



function getEnvAdvice(paramType: string, value: number, config: any) {
  if (value == null || config == null) return '无数据或设备异常';
  // 只有非风速参数才把0视为无数据
  if (value === 0 && paramType !== 'wind_speed') return '无数据或设备异常';
  const min = config.thresholdMin;
  const max = config.thresholdMax;
  if (value >= min && value <= max) {
    if (paramType === 'ph_value') return 'pH适宜，利于生长';
    if (paramType === 'dissolved_oxygen') return '溶解氧充足，水体健康';
    if (paramType === 'ammonia_nitrogen') return '氨氮正常，水质良好';
    if (paramType === 'water_temperature') return '水温适宜，利于生长';
    if (paramType === 'light_intensity') return '光照适宜';
    if (paramType === 'humidity') return '湿度适宜';
    if (paramType === 'temperature') return '温度适宜';
    if (paramType === 'wind_speed') return '风速适宜';
    return '环境适宜';
  }
  if (value < min) {
    if (paramType === 'ph_value') return `pH ${value}（偏酸）可能影响鱼类呼吸`;
    if (paramType === 'dissolved_oxygen') return `溶解氧偏低 可能导致鱼类缺氧`;
    if (paramType === 'ammonia_nitrogen') return `氨氮偏低 关注水体变化`;
    if (paramType === 'water_temperature') return `水温偏低 可能影响生长`;
    if (paramType === 'light_intensity') return `光照偏低 影响光合作用`;
    if (paramType === 'humidity') return `湿度偏低 需关注环境`;
    if (paramType === 'temperature') return `温度偏低 需关注环境`;
    if (paramType === 'wind_speed') return `风速偏低 关注通风`;
    return '数值偏低，需关注';
  }
  if (value > max) {
    if (paramType === 'ph_value') return `pH ${value}（偏碱）可能影响鱼类健康`;
