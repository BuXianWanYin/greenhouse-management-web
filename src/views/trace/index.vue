    task: taskList[0]?.batchTask?.taskName || '',
    operator: taskList[0]?.batchTask?.responsiblePersonName || '',
    operationDetail: foodInfo?.description || '',
    environment: {
      temperature: taskList[0]?.weatherAvg?.temperature ?? 0,
      humidity: taskList[0]?.weatherAvg?.humidity ?? 0,
      ph: taskList[0]?.waterQualityAvg?.phValue ?? 0,
      dissolvedOxygen: taskList[0]?.waterQualityAvg?.dissolvedOxygen ?? 0,
      lightIntensity: taskList[0]?.weatherAvg?.lightIntensity ?? 0,
      windSpeed: taskList[0]?.weatherAvg?.windSpeed ?? 0,
      waterTemperature: taskList[0]?.waterQualityAvg?.waterTemperature ?? 0,
      ammoniaNitrogen: taskList[0]?.waterQualityAvg?.ammoniaNitrogen ?? 0,
      nitrite: 0,
